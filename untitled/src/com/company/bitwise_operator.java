package com.company;

public class bitwise_operator {
    public static void main(String[] args) {

        int a=3;
        int b =101;
        System.out.println(a%b);
        System.out.println(a|b);
        System.out.println(~a);
        System.out.println(a<<2);
        System.out.println(a>>1);
    }
}
