public class floyds_tiangle {
    public static void main(String[] args) {
        int n =5;
        int no=1;
        int i,j;
        for (i=1;i<=n;i++){
            for (j=1;j<=i;j++){
                System.out.print(no +" ");
                no++;
            }
            System.out.println();
        }


    }
    }

